export type PassType = "dpa" | "special";

export function getAttractionPasses(id: number): PassType[] {
  const passes: PassType[] = [];
  if (DPA_IDS.has(id)) passes.push("dpa");
  if (SPECIAL_PASS_IDS.has(id)) passes.push("special");
  return passes;
}

export function getAreaGuide(park: "tdl" | "tds", slug: string): AreaGuide | undefined {
  const guides = park === "tdl" ? TDL_AREA_GUIDES : TDS_AREA_GUIDES;
  return guides.find((g) => g.slug === slug);
}

export function getAllAreaSlugs(park: "tdl" | "tds"): string[] {
  const guides = park === "tdl" ? TDL_AREA_GUIDES : TDS_AREA_GUIDES;
  return guides.map((g) => g.slug);
}
